						<p>The latest Hotspotting Price Predictor Index has picked the biggest chunk of metropolitan Brisbane growth would come out of the Moreton Bay area. The Bay toppled Logan City as the top municipality in the capital city region, holding 10 of Brisbane’s 29 growth suburbs.</p>
						<p>Hotspotting analyst Terry Ryder said the index tracked increased sales volumes which were one of the best indicators that price rises were set to rise.</p>
						<p>Moreton Bay’s hottest pick was Redcliffe where houses as well as unit prices were predicted to rise. Every other suburb on the list was expected to see house prices rise including Arana Hills; Banksia Beach, Beachmere; Bellara; Bray Park; Deception Bay; Narangba; Ningi; and Rothwell.</p>
						<p>In the surging inner-city, Bardon was the only suburb where prices were predicted to see a steady rise, joined by northside suburbs Bald Hills, Brighton, Fitzgibbon and Hendra.</p>
						<p>On the southside, Darra, Eight Mile Plains and Heathwood were hot picks, as well as Redland’s Birkdale, Redland Bay and Victoria Point.</p>
						<p>Logan had six suburbs set to rise steadily including Edens Landing, Jimboomba, Slacks Creek, Waterford, Windaroo and Yarrabilba, while Ipswich had two on the list - Raceview and Redbank Plains.</p>
						<p>Redcliffe real estate agent Bruce Robson of Coronis said the suburb was seeing a rise in buyer inquiries.</p>
						<p>“Redcliffe has been the most affordable waterfront suburb in Brisbane with huge potential for the future,” he said. “It will be the bayside suburb for the new northern hub.”</p>
						<p>Rhonda Subloo, who put her home at 2 Walsh Street on the market for $695,000, has no plans to leave the area.</p>
						<p>“Since the train’s come to Redcliffe there will be more people looking to come in here. Prices will go up,” she said. “We’re looking around in Redcliffe for a retirement village type setting. We like to be around the sea.”</p>
